    {
        $cliente = new Cliente([
            'nome' => $request->input('nome'),
            'endereco' => $request->input('endereco'),
            'contato' => $request->input('contato')
        ]);
        $cliente->save();

        $produtos = new Produtos([
            'nome' => $request->input('nome'),
            'preco' => $request->input('preco'),
            'quantidade_estoque' => $request->input('quantidade_estoque')
        ]);
        $produtos->save();
    }

